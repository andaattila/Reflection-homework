public class Task6 {
    public static void main(String[] args) {
        System.out.println("Writes every number between -50 to 50 to the console!");

        int startNr = -50;
        int endNr = 50;

        for (int i = startNr; i <= endNr; i++) {
            System.out.println(i);
        }
    }
}
